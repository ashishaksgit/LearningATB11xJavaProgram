package ex_01_Java_Basic;

public class _ABC_abc {
    public static void main(String[] args) {
        //No, _ABC_abc is not a good class name in Java. While it is syntactically valid, it does not follow Java naming conventions.
        //Underscore at the beginning (_ABC) is unusual – Java class names typically do not start with underscores.
        //Mixed uppercase and lowercase in a non-standard way – Java class names should follow PascalCase (CamelCase with the first letter capitalized).
        //Underscore in the middle (_abc) is unnecessary – Java class names should generally avoid underscores unless in constants.
        System.out.println("_ABC_abc ");
    }
}
